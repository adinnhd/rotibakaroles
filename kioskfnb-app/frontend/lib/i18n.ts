export function getCategoryName(id: string, language: Language): string {
  const names = translations[language].categoryNames as Record<string, string>
  return names[id] ?? id
}

export function getMenuName(name: string, nameEn: string | undefined, language: Language): string {
  return language === "en" && nameEn ? nameEn : name
}

export function getMenuDescription(description: string, descriptionEn: string | undefined, language: Language): string {
  return language === "en" && descriptionEn ? descriptionEn : description
}

